	<h1>Getting to Singing Beach</h1>

	<h2>By Car</h2> 
<p>Take route 95/128 north. Stay on route 128, and take exit 16 (Pine Street/Manchester exit). Take a left at the end of the ramp. Follow to the end. Take a left onto Route 127. You will enter downtown less than ¼ mile from there. Follow until you come to an intersection of a gas station, Crosby’s Market, and the post office. At this point Route 127 takes a sharp left turn&emsp;do not follow route 127 any further. Continue straight over the railroad tracks onto Beach Street. Follow Beach Street 1/2 a mile to the end. Welcome to Singing Beach. Enjoy!<p>
	<h3>Parking</h3>
	<p>Parking in Manchester is extremely limited on the weekends. On Fridays, Saturdays and Sundays, parking at Singing Beach is limited to residents only with valid a Beach Parking Sticker. There is no non-resident parking at the beach on those days. Beginning Monday June 11th, Non-Residents can pay to park at Singing Beach on Mondays - Thursdays if space is available.(Not valid on Tuesday, July 3rd or Wednesday, July 4th). The cost to park at Singing Beach is $25.00 per day. On weekdays if the parking lot is filled or on all weekends additional parking may be available in downtown Manchester. There are some parking spaces at Masconomo Park (near the Train Station), there are 2 hour parking meters throughout the downtown and on Saturdays and Sundays, the Boy Scouts sell parking spaces at the Train Station for $25.00 per car. Public transportation is highly encouraged. Free parking is available in the Singing Beach Parking Lot between 7:00 pm - 10:00 p.m.</p>
